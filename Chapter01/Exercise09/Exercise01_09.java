public class Exercise01_09{
    public static void main(String[] args) {
        double width = 5.3;
        double height = 8.6;

        double area = width * height;
        double perimeter = 2 * (width + height);

        System.out.println("The rectangle area of width " + width + " and height " + height + " is " + area);
        System.out.println("The rectangle perimeter of width " + width + " and height " + height + " is " + perimeter);
    }
}
